document.getElementById('notifyButton').addEventListener('click', function() {
    // Создаем новое уведомление
    const notification = document.createElement('div');
    notification.classList.add('notification');
    notification.textContent = 'Это ваше уведомление!';

    // Добавляем уведомление в контейнер
    document.getElementById('notificationContainer').appendChild(notification);

    // Убираем уведомление через 3 секунды
    setTimeout(() => {
        notification.classList.add('hide');
        // Удаляем уведомление из DOM после завершения анимации
        setTimeout(() => {
            notification.remove();
        }, 500); // Время соответствует времени анимации
    }, 3000); // Уведомление исчезает через 3 секунды
});

/**1. HTML создает кнопку для показа уведомлений и контейнер для самих уведомлений.

2. CSS стилизует уведомления, делая их зелеными с белым текстом и добавляя тень для лучшего визуального восприятия. Также добавлены стили для анимации исчезновения.

3. JavaScript обрабатывает событие клика по кнопке, создавая новое уведомление, которое добавляется в контейнер. Уведомление исчезает через три секунды с эффектом плавного исчезновения.

▎Использование:

Скопируйте код в три отдельных файла (HTML, CSS и JavaScript) и откройте HTML файл в браузере. Нажмите на кнопку "Показать уведомление", чтобы увидеть уведомление в правом верхнем углу экрана. Уведомление исчезнет через три секунды. */